# run_all_digital_knowledge_system_workflows.R
# Run all R workflows for Frameworks for Digital Knowledge Systems.

args <- commandArgs(trailingOnly = FALSE)
file_arg <- grep("^--file=", args, value = TRUE)

if (length(file_arg) > 0) {
  script_path <- normalizePath(sub("^--file=", "", file_arg[1]), mustWork = TRUE)
  article_root <- normalizePath(file.path(dirname(script_path), ".."), mustWork = TRUE)
} else {
  article_root <- getwd()
}

source(file.path(article_root, "r", "catalyst_canvas_digital_knowledge_system_analysis.R"))

message("All R digital-knowledge-system workflows complete.")
